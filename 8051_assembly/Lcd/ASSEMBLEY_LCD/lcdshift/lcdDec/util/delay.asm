;wait_5ms
;wait_20ms
;wait_1sec
;--------------------------------------------------------------------
;-----------------------------------------------
wait_5ms:
	mov	dcount_2,#21d
	mov	dcount_1,#0ffh		;$-3-3
	djnz	dcount_1,$		;$-3
	djnz	dcount_2,$-3-3
	ret
;-----------------------------------------------
;-----------------------------------------------
wait_20ms:
	mov	dcount_2,#55h
	mov	dcount_1,#0ffh		;$-3-3
	djnz	dcount_1,$		;$-3
	djnz	dcount_2,$-3-3
	ret
;-----------------------------------------------
;----------------------------------------------- 
wait_1sec:
	mov	dcount_3,#07d
l2:	mov 	dcount_2,#0ffh
l1:	mov 	dcount_1,#0ffh
	djnz 	dcount_1,$
	djnz 	dcount_2,l1
	djnz 	dcount_3,l2
	ret
;-----------------------------------------------
